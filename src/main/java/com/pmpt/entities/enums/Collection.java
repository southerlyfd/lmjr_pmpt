package com.pmpt.entities.enums;

/**
 * @ClassName: Collection
 * @Description: 是否收藏
 * @Author: 汪洋
 * @Date: 2017年8月16日 下午1:26:24
 */
public enum Collection {

	NOTCOLLECTION("0", "未收藏"), 
	ISCOLLECTION("1", "已收藏");

	private String key;

	private String des;

	Collection(String key, String des) {
		this.key = key;
		this.des = des;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getDes() {
		return des;
	}

	public void setDes(String des) {
		this.des = des;
	}

}
